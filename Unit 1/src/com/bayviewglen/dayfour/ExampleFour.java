package com.bayviewglen.dayfour;

public class ExampleFour {

	public static void main(String[] args) {
		int x = 7; 
		
		System.out.println(x);
		System.out.println(++x);
		System.out.println(x);
		
		int y = 6; 
		
		System.out.println(y);
		int z = ++y + 10; 
		
		System.out.println(y);
		System.out.println(z);

		//--y and y--
		
		//x++ and ++x is the same as x = x + 1; 
		
		x = x + 17; 
		
		x += 17; 
		x -= 17; 
		
		x *= 17; 
		x /= 17;
		
		x %= 17; 
		
		
	}

}
